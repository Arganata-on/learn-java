public class Mahasiswa {
    private String nama;
    private String nomerIndukMahasiswa;
    private String jurusan;

    public String getNam() {
        return nama;
    }

    public String getNIM() {
        return nomerIndukMahasiswa;
    }

    public String getJur() {
        return jurusan;
    }

    public void setNam(String nama) {
        this.nama = nama;
    }

    public void setNIM(String nomerIndukMahasiswa) {
        this.nomerIndukMahasiswa = nomerIndukMahasiswa;
    }

    public void setJur(String jurusan) {
        this.jurusan = jurusan;
    }
}
